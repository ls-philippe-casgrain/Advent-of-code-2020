    func testExampleTwo() throws {
        let treeMap = TreeMap(input: ["..##.......",
                                      "#...#...#..",
                                      ".#....#..#.",
                                      "..#.#...#.#",
                                      ".#...##..#.",
                                      "..#.##.....",
                                      ".#.#.#....#",
                                      ".#........#",
                                      "#.##...#...",
                                      "#...##....#",
                                      ".#..#...#.#"])

        let oneOne = treeMap.numTrees(right: 1, down: 1)
        XCTAssertEqual(oneOne, 2)
        let threeOne = treeMap.numTrees(right: 3, down: 1)
        XCTAssertEqual(threeOne, 7)
        let fiveOne = treeMap.numTrees(right: 5, down: 1)
        XCTAssertEqual(fiveOne, 3)
        let sevenOne = treeMap.numTrees(right: 7, down: 1)
        XCTAssertEqual(sevenOne, 4)
        let oneTwo = treeMap.numTrees(right: 1, down: 2)
        XCTAssertEqual(oneTwo, 2)

        XCTAssertEqual(oneOne * threeOne * fiveOne * sevenOne * oneTwo, 336)
    }

